  <p class="fulloverview"> Squad Vehicle ‘Charlie’ is a light infantry squad vehicle that is fully modular and is a platform for various applications. It can be configured into a troop carrier, recon vehicle, EMS, logistics, fire truck, mobile water filtration, mobile generator, gun turret, mobile rocket-launcher, or a general-purpose vehicle.  It is also designed for quick ingress and egress, airlifting for rapid deployment, and has full off-road capabilities.
